    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(!validateInputs()){
             JOptionPane.showMessageDialog(null,"Please all the fields","Incomplete data!",JOptionPane.ERROR_MESSAGE);
             return;

        }
        try{
        Employeepojo emp=new Employeepojo();
        emp.setEmpid(jbEmpId.getSelectedItem().toString());
        emp.setEmpname(txtEmpName.getText());
        emp.setJob(jbJob.getSelectedItem().toString());
        emp.setSalary(Double.parseDouble(txtSal.getText()));
        boolean result=EmployeeDAO.updateEmployee(emp);
        if(result)
                      JOptionPane.showMessageDialog(null,"Records are updated!","Updation done!",JOptionPane.INFORMATION_MESSAGE);
   else
                         JOptionPane.showMessageDialog(null,"Records not updated","Error!",JOptionPane.ERROR_MESSAGE);
    
        }
      catch(SQLException ex){
             JOptionPane.showMessageDialog(null,"DB Error!","Error!",JOptionPane.ERROR_MESSAGE);
         ex.printStackTrace();
        }  
    }//GEN-LAST:event_btnUpdateActionPerformed

