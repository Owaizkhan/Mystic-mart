    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(!validateInputs())
            return;
        try{
            String userid=jbRecepId.getSelectedItem().toString();
            boolean result=ReceptionistDAO.UpdatePassword(userid,Password);
            if(result){
                JOptionPane.showMessageDialog(null,"Password is Changed!");
                clearText();
                jbRecepId.setSelectedIndex(0);
             return ;
            }
            
            JOptionPane.showMessageDialog(null,"Password is not Changed!");
                clearText();
        }
        catch(SQLException ex){
             JOptionPane.showMessageDialog(null,"DB Error!","Error!",JOptionPane.ERROR_MESSAGE);
         ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

