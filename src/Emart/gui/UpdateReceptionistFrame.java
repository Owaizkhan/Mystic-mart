    private void loadReceptionistDetails() {
        try{
            receptionist=ReceptionistDAO.getAllReceptionistId();
            if(receptionist.isEmpty()){
                             JOptionPane.showMessageDialog(null,"Receptionist is not present");
                             return;

            }
         Set<String> keys=receptionist.keySet();
         for(String id:keys){
             jbRecepId.addItem(id);
         }
            
        }
        catch(SQLException ex){
             JOptionPane.showMessageDialog(null,"DB Error!","Error!",JOptionPane.ERROR_MESSAGE);
         ex.printStackTrace();
        }
    }
    private boolean validateInputs(){
        char[]pwd=txtPassword.getPassword();
        char[]rpwd=txtRepassword.getPassword();
        if(pwd.length<4||rpwd.length<4){
           
             JOptionPane.showMessageDialog(null,"Password must be 4 length!");
             return false;
         
        
        }
        Password=String.valueOf(pwd);
        String repassword=String.valueOf(rpwd);
        if(!Password.equals(repassword)){
            JOptionPane.showMessageDialog(null,"Password must be same!");
             return false;
        }
        return true;
    }

    private void clearText() {
        txtPassword.setText("");
        txtRepassword.setText("");
    }
