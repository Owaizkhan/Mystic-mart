    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
         if(!validateInputs()){
            JOptionPane.showMessageDialog(null,"Please all the fields","Incomplete data!",JOptionPane.ERROR_MESSAGE);
             return;
        }
        try{
            int n=jbTax.getSelectedItem().toString().length();
            int tax=Integer.parseInt(jbTax.getSelectedItem().toString().substring(0, n-1));
            Productspojo p=new Productspojo();
            p.setProductId(txtProductId.getText().trim());
            p.setProductName(txtProductName.getText().trim());
            p.setProductCompany(txtCompName.getText().trim());
            p.setProductPrice(Double.parseDouble(txtProductPrice.getText().trim()));
            p.setOurPrice(Double.parseDouble(txtOurPrice.getText().trim()));
            p.setQuantity(Integer.parseInt(txtQuantity.getText().trim()));
            p.setTax(tax);
            boolean result=ProductDAO.updateProduct(p);
             if(result){
            JOptionPane.showMessageDialog(null,"Products Updated succesfully!","Success!",JOptionPane.INFORMATION_MESSAGE);
            
tmt.setRowCount(0);
loadProductDetail();

           
               return;
            }
              JOptionPane.showMessageDialog(null,"Products  not updated!","Failed!",JOptionPane.ERROR_MESSAGE);

            
        }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Please Input Numeric value","Conversion Error!",JOptionPane.ERROR_MESSAGE);
         ex.printStackTrace();
        }
       
        catch(SQLException ex)
       {
         JOptionPane.showMessageDialog(null,"DB Error!","Error!",JOptionPane.ERROR_MESSAGE);
         ex.printStackTrace(); 
       }
    }//GEN-LAST:event_btnUpdateActionPerformed

