    private void loadProductDetail() {
        try{
            List<Productspojo> stockList=ProductDAO.getProductDetails();
            if(stockList.isEmpty()){
                JOptionPane.showMessageDialog(null, "Product is not present in stock");
                return;
            }
             
            Object []rows=new Object[7];
                         tmt=(DefaultTableModel)jProductList.getModel();

            for(Productspojo stock:stockList){
                rows[0]=stock.getProductId();
                rows[1]=stock.getProductName();
                rows[2]=stock.getProductCompany();
                rows[3]=stock.getProductPrice();
                rows[4]=stock.getOurPrice();
                rows[5]=stock.getQuantity();
                rows[6]=stock.getTax()+"%";
                tmt.addRow(rows);
            }
    }
        catch(SQLException ex){

                    JOptionPane.showMessageDialog(null,"DB Error!","Error!",JOptionPane.ERROR_MESSAGE);
ex.printStackTrace();
        }
    }
